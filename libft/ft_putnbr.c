#include <unistd.h>
#define MIN_INT -2147483648

void	ft_putnbr(int n)
{
	char	c;

	if (n == MIN_INT)
	{
		ft_putnbr(-214748364);
		n = 8;
	}
	(n < 0) ? (void)((n *= -1) && write(1, "-", 1)) : 0;
	(n < 10) ? (void)((c = '0' + n) && write(1, &c, 1)) : 0;
	if (n >= 10)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
}
